			$('#numeric').hide();
			$('#formula').show();
			$('#conditional_formula').hide();
		 }
		 else if($(this).val()==='Conditional Formula')
		 {
			$('#numeric').hide();
			$('#formula').hide();
			$('#conditional_formula').show();
		 }
	});
	
});
